            // Enhanced interaction for program items
            document.addEventListener('DOMContentLoaded', function() {
                const programItems = document.querySelectorAll('.program-item');
                const programContents = document.querySelectorAll('.program-content');
                const programDescriptions = document.querySelectorAll('.program-description');
                
                // Function to show program content and description
                function showProgramContent(programId) {
                    // Hide all program contents
                    programContents.forEach(content => {
                        content.classList.add('hidden');
                        content.classList.remove('active');
                    });
                    
                    // Hide all program descriptions
                    programDescriptions.forEach(desc => {
                        desc.classList.add('hidden');
                        desc.classList.remove('active');
                    });
                    
                    // Show selected program content
                    const targetContent = document.getElementById(`${programId}-content`);
                    if (targetContent) {
                        targetContent.classList.remove('hidden');
                        targetContent.classList.add('active');
                    }
                    
                    // Show selected program description
                    const targetDescription = document.getElementById(`${programId}-description`);
                    if (targetDescription) {
                        targetDescription.classList.remove('hidden');
                        targetDescription.classList.add('active');
                    }
                    
                    // Update background gradient based on program
                    const programImage = document.querySelector('.program-image');
                    switch(programId) {
                        case 'tkl':
                            programImage.className = 'program-image rounded-2xl overflow-hidden bg-white/70 aspect-square flex items-center justify-center transition-all duration-500 relative shadow-lg';
                            break;
                        case 'pplg':
                            programImage.className = 'program-image rounded-2xl overflow-hidden bg-white/70 aspect-square flex items-center justify-center transition-all duration-500 relative shadow-lg';
                            break;
                        case 'tkro':
                            programImage.className = 'program-image rounded-2xl overflow-hidden bg-white/70 aspect-square flex items-center justify-center transition-all duration-500 relative shadow-lg';
                            break;
                        case 'tbsm':
                            programImage.className = 'program-image rounded-2xl overflow-hidden bg-white/70 aspect-square flex items-center justify-center transition-all duration-500 relative shadow-lg';
                            break;
                        case 'akl':
                            programImage.className = 'program-image rounded-2xl overflow-hidden bg-white/70 aspect-square flex items-center justify-center transition-all duration-500 relative shadow-lg';
                            break;
                        case 'mplb':
                            programImage.className = 'program-image rounded-2xl overflow-hidden bg-white/70 aspect-square flex items-center justify-center transition-all duration-500 relative shadow-lg';
                            break;
                        default:
                            programImage.className = 'program-image rounded-2xl overflow-hidden bg-white/70 aspect-square flex items-center justify-center transition-all duration-500 relative shadow-lg';
                    }
                }
                
                programItems.forEach(item => {
                    item.addEventListener('click', function() {
                        // Remove active class from all items
                        programItems.forEach(i => {
                            i.style.backgroundColor = '';
                        });
                        
                        // Add active style to clicked item
                        this.style.backgroundColor = '#f8fafc';
                        
                        // Show corresponding program content and description
                        const programId = this.getAttribute('data-program');
                        showProgramContent(programId);
                    });
                });
                
                // Add keyboard navigation
                document.addEventListener('keydown', function(e) {
                    if (e.key === 'ArrowDown' || e.key === 'ArrowUp') {
                        e.preventDefault();
                        
                        const currentActive = document.querySelector('.program-item[style*="background-color"]');
                        let nextItem;
                        
                        if (e.key === 'ArrowDown') {
                            nextItem = currentActive ? currentActive.nextElementSibling : programItems[0];
                        } else {
                            nextItem = currentActive ? currentActive.previousElementSibling : programItems[programItems.length - 1];
                        }
                        
                        if (nextItem) {
                            nextItem.click();
                            nextItem.scrollIntoView({ behavior: 'smooth', block: 'nearest' });
                        }
                    }
                });
            });
