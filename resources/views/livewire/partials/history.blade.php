    <script>
        document.addEventListener('DOMContentLoaded', function() {
            const timelineItems = document.querySelectorAll('.timeline-item');
            const timelineContents = document.querySelectorAll('.timeline-content');
            const timelineProgress = document.querySelector('.timeline-progress');
            
            // Set initial active state
            setActiveYear('2004');
            
            // Add click event to timeline items
            timelineItems.forEach(item => {
                item.addEventListener('click', function() {
                    const year = this.getAttribute('data-year');
                    setActiveYear(year);
                });
            });
            
            function setActiveYear(year) {
                // Update timeline items
                timelineItems.forEach(item => {
                    if (item.getAttribute('data-year') === year) {
                        item.classList.add('active');
                    } else {
                        item.classList.remove('active');
                    }
                });
                
                // Update timeline contents
                timelineContents.forEach(content => {
                    if (content.getAttribute('data-year') === year) {
                        content.classList.add('active');
                    } else {
                        content.classList.remove('active');
                    }
                });
                
                // Update timeline progress position
                const activeIndex = Array.from(timelineItems).findIndex(item => 
                    item.getAttribute('data-year') === year
                );
                
                const progressWidth = (activeIndex / (timelineItems.length - 1)) * 100;
                timelineProgress.style.width = `${progressWidth}%`;
            }
        });
    </script>

